#Entrada de Dados usuario entrara com a informação pedida no caso os números
n1=float(input("Digite o primeiro Número: "))
n2=float(input("Digite o segundo Número: "))
#processando os dados foi atribuido que o programa pegue as informações passadas
#pelo usuario somando e multiplicando os números digitados
soma=n1+n2
mult=n1*n2
#Saída de Dados Exibindo resultado tando ta soma como da multiplição do números
print("O Resultado Somando os dois Números é ",soma)
print("O Resultado Multiplicando os dois Números é ",mult)
